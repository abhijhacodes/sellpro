const API = "http://localhost:5000/api";

export const getVerifiedProducts = () => {
  return fetch(`${API}/products`, { method: "GET" })
    .then((response) => {
      return response.json();
    })
    .catch((err) => console.log(err));
};

export const getProductPhotoURL = (productId) => {
  const imageURL = productId
    ? `${API}/product/photo/${productId}`
    : "/assets/logo.png";
  return imageURL;
};

export const uploadProduct = (userId, token, product) => {
  return fetch(`${API}/product/upload/${userId}`, {
    method: "POST",
    headers: {
      Accept: "application/json",
      Authorization: `Bearer ${token}`,
    },
    body: product,
  })
    .then((response) => {
      return response.json();
    })
    .catch((err) => console.log(err));
};

